class TestimonialData {
  final String image;
  final String name;
  final String country;
  final String flag;
  final String program;
  final String review;
  final String result;
  final String date;
  final bool verified;

  const TestimonialData({
    required this.image,
    required this.name,
    required this.country,
    required this.flag,
    required this.program,
    required this.review,
    required this.result,
    required this.date,
    required this.verified,
  });
}